package week1homeworks;

public class LearningOfUsingMethods {
	int number1 ;
	int number2 ;
	void add(int number1,int number2)
	{
		int number3 = number1 + number2 ;
		System.out.println("Addition of two numbers :" +number3);
	}
	void sub(int number1,int number2)
	{
		int number5 = number1 - number2 ;
		System.out.println("Subtraction of two numbers :" +number5);	
	}
	void multiply(int number1,int number2)
	{
		int number6 = number1 * number2 ;
		System.out.println("Multiplication of two numbers :" +number6);
	}
	void divide(int number1,int number2)
	{
		int number7 = number1 / number2 ;
		System.out.println("Division of two numbers :" +number7);
	}
}
